    private void AddbtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_AddbtnMouseClicked
        // TODO add your handling code here:
     try {
        String name = EName.getText();
        String type = EType.getText();
        BigDecimal price = new BigDecimal(EPrice.getText());
        java.util.Date purchaseDate = EPurchaseDate.getDate();
        String condition = EConditionText.getText();

        if (name.isEmpty() || type.isEmpty() || price == null || purchaseDate == null || condition.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter all fields");
            return;
        }

        String sql = "INSERT INTO Equipment (Name, Type, PurchaseDate, Price, Condition) VALUES (?, ?, ?, ?, ?)";
        pst = con.prepareStatement(sql);
        pst.setString(1, name);
        pst.setString(2, type);
        pst.setDate(3, new java.sql.Date(purchaseDate.getTime()));
        pst.setBigDecimal(4, price);
        pst.setString(5, condition);

        pst.executeUpdate();
        JOptionPane.showMessageDialog(this, "Equipment Added Successfully");
        DisplayEquipments();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error Adding Equipment: " + e.getMessage());
    }
      
    }//GEN-LAST:event_AddbtnMouseClicked

