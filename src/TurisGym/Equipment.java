    private void EditbtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_EditbtnMouseClicked
        // TODO add your handling code here:
        try {
        int selectedRow = EquipmentTable.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select an equipment to edit.");
            return;
        }

        int equipmentID = Integer.parseInt(EquipmentTable.getValueAt(selectedRow, 0).toString());
        String name = EName.getText();
        String type = EType.getText();
        BigDecimal price = new BigDecimal(EPrice.getText());
        java.util.Date purchaseDate = EPurchaseDate.getDate();
        String condition = EConditionText.getText();

        String sql = "UPDATE Equipment SET Name=?, Type=?, PurchaseDate=?, Price=?, Condition=? WHERE EquipmentID=?";
        pst = con.prepareStatement(sql);
        pst.setString(1, name);
        pst.setString(2, type);
        pst.setDate(3, new java.sql.Date(purchaseDate.getTime()));
        pst.setBigDecimal(4, price);
        pst.setString(5, condition);
        pst.setInt(6, equipmentID);

        pst.executeUpdate();
        JOptionPane.showMessageDialog(this, "Equipment updated successfully.");
        DisplayEquipments();
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, "Error updating equipment: " + ex.getMessage());
    }
    }//GEN-LAST:event_EditbtnMouseClicked

    private void DeleteBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_DeleteBtnMouseClicked
        // TODO add your handling code here:
      try {
        int selectedRow = EquipmentTable.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select an equipment to delete.");
            return;
        }

        int confirm = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this equipment?", "Delete Equipment", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            int equipmentID = Integer.parseInt(EquipmentTable.getValueAt(selectedRow, 0).toString());
            String sql = "DELETE FROM Equipment WHERE EquipmentID=?";
            pst = con.prepareStatement(sql);
            pst.setInt(1, equipmentID);
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Equipment deleted successfully.");
            DisplayEquipments();
        }
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, "Error deleting equipment: " + ex.getMessage());
    }
    }//GEN-LAST:event_DeleteBtnMouseClicked

