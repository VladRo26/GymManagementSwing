    private void DisplayEquipments() {
    try {
        String sql = "SELECT EquipmentID, Name, Type, PurchaseDate, Price, Condition FROM Equipment";
        pst = con.prepareStatement(sql);
        ResultSet rs = pst.executeQuery();
        DefaultTableModel model = (DefaultTableModel) EquipmentTable.getModel();
        model.setRowCount(0);  // Clear existing data

        while (rs.next()) {
            Vector<Object> row = new Vector<>();
            row.add(rs.getInt("EquipmentID"));
            row.add(rs.getString("Name"));
            row.add(rs.getString("Type"));
            row.add(rs.getDate("PurchaseDate"));
            row.add(rs.getBigDecimal("Price"));
            row.add(rs.getString("Condition"));
            model.addRow(row);
        }
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, "Error loading equipment: " + ex.getMessage());
    }
    }

