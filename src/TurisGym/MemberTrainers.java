    private void AddbtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_AddbtnMouseClicked
        // TODO add your handling code here:
            try {
        String selectedMember = (String) MTComboBox.getSelectedItem();
        int memberId = Integer.parseInt(selectedMember.split(" - ")[1]); // Extract MemberID
        java.util.Date startDate = MTStartDate.getDate();
        java.util.Date endDate = MTEndDate.getDate();
        
          if (endDate != null && startDate != null && !endDate.after(startDate)) {
            JOptionPane.showMessageDialog(this, "End Date must be after Start Date.");
            return; // Stop the method execution if the condition is not met
        }
        
        String sql = "INSERT INTO MemberTrainers (TrainerID, MemberID, StartPeriod, EndPeriod) VALUES (?, ?, ?, ?)";
        PreparedStatement pst = con.prepareStatement(sql);
        pst.setInt(1, trainerId);
        pst.setInt(2, memberId);
        pst.setDate(3, new java.sql.Date(startDate.getTime()));
        pst.setDate(4, new java.sql.Date(endDate.getTime()));        
        
        int added = pst.executeUpdate();
        if (added > 0) {
            JOptionPane.showMessageDialog(this, "Member assigned successfully!");
            loadMembersForTrainer(); // Refresh the table
            loadAvailableMembers(); // Refresh the available members
             MembersTrainerTable.revalidate();
             MembersTrainerTable.repaint();
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error assigning member: " + e.getMessage());
    }
    }//GEN-LAST:event_AddbtnMouseClicked

