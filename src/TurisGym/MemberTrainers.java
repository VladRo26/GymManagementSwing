private void loadMembersForTrainer() {
   String sql = "SELECT mt.MemberID, m.FirstName, m.LastName, mt.StartPeriod, mt.EndPeriod " +
                 "FROM MemberTrainers mt " +
                 "JOIN Members m ON mt.MemberID = m.MemberID " +
                 "WHERE mt.TrainerID = ?";
    try (PreparedStatement pst = con.prepareStatement(sql)) {
        pst.setInt(1, trainerId);
        ResultSet rs = pst.executeQuery();
        DefaultTableModel model = (DefaultTableModel) MembersTrainerTable.getModel();
        model.setRowCount(0); // Clear existing data

        while (rs.next()) {
            Object[] row = {
                rs.getInt("MemberID"),
                rs.getString("FirstName") + " " + rs.getString("LastName"), // Combining first and last names
                rs.getDate("StartPeriod"),
                rs.getDate("EndPeriod")
            };
            model.addRow(row);
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Error loading data: " + e.getMessage());
    }
}


       
private void loadAvailableMembers() {
  String sql = "SELECT MemberID, FirstName, LastName FROM Members m " +
                 "WHERE NOT EXISTS (" +
                 "SELECT 1 FROM MemberTrainers mt WHERE mt.MemberID = m.MemberID AND mt.EndPeriod >= CAST(GETDATE() AS DATE)" +
                 ")";
    try {
        PreparedStatement pst = con.prepareStatement(sql);
        ResultSet rs = pst.executeQuery();
        MTComboBox.removeAllItems(); // Clear existing items
        boolean hasAvailableMembers = false;
        while (rs.next()) {
            hasAvailableMembers = true;
            String memberName = rs.getString("FirstName") + " " + rs.getString("LastName");
            MTComboBox.addItem(memberName + " - " + rs.getInt("MemberID"));
        }
        if (!hasAvailableMembers) {
            MTComboBox.addItem("No Available Members");
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Error loading members: " + e.getMessage());
    }
    
}



