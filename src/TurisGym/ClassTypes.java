    private void AddbtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_AddbtnMouseClicked
        // TODO add your handling code here:
            try {
            String typeName = MEName.getText();
            String description = MEDescription.getText();

            if (typeName.isEmpty() || description.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter all fields");
                return;
            }

            String sql = "INSERT INTO ClassTypes (TypeName, Description) VALUES (?, ?)";
            pst = con.prepareStatement(sql);
            pst.setString(1, typeName);
            pst.setString(2, description);

            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Class Type Added Successfully");
            DisplayClassTypes();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error Adding Class Type: " + e.getMessage());
        }
    }//GEN-LAST:event_AddbtnMouseClicked

