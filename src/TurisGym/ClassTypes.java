    private void EditbtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_EditbtnMouseClicked
        // TODO add your handling code here:
         try {
            int selectedRow = EquipmentTable.getSelectedRow();
            if (selectedRow == -1) {
                JOptionPane.showMessageDialog(this, "Please select a class type to edit.");
                return;
            }

            int classTypeID = Integer.parseInt(EquipmentTable.getValueAt(selectedRow, 0).toString());
            String typeName = MEName.getText();
            String description = MEDescription.getText();

            String sql = "UPDATE ClassTypes SET TypeName=?, Description=? WHERE ClassTypeID=?";
            pst = con.prepareStatement(sql);
            pst.setString(1, typeName);
            pst.setString(2, description);
            pst.setInt(3, classTypeID);

            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Class Type updated successfully.");
            DisplayClassTypes();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Error updating class type: " + ex.getMessage());
        }
    }//GEN-LAST:event_EditbtnMouseClicked

    private void DeleteBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_DeleteBtnMouseClicked
        // TODO add your handling code here:
             try {
        int selectedRow = EquipmentTable.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select a class type to delete.");
            return;
        }

        int classTypeID = Integer.parseInt(EquipmentTable.getValueAt(selectedRow, 0).toString());
        
        // Inform the user about the cascading delete effect
        String message = "Deleting this class type will also delete all associated classes. Do you want to proceed?";
        int confirm = JOptionPane.showConfirmDialog(this, message, "Delete Class Type", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);
        
        if (confirm == JOptionPane.YES_OPTION) {
            String sql = "DELETE FROM ClassTypes WHERE ClassTypeID=?";
            pst = con.prepareStatement(sql);
            pst.setInt(1, classTypeID);
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Class Type deleted successfully along with its associated classes.");
            DisplayClassTypes();
        }
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, "Error deleting class type: " + ex.getMessage());
    }
    }//GEN-LAST:event_DeleteBtnMouseClicked

