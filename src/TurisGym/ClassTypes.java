      private void DisplayClassTypes() {
        try {
            String sql = "SELECT ClassTypeID, TypeName, Description FROM ClassTypes";
            pst = con.prepareStatement(sql);
            ResultSet rs = pst.executeQuery();
            DefaultTableModel model = (DefaultTableModel) EquipmentTable.getModel();
            model.setRowCount(0);

            while (rs.next()) {
                Vector<Object> row = new Vector<>();
                row.add(rs.getInt("ClassTypeID"));
                row.add(rs.getString("TypeName"));
                row.add(rs.getString("Description"));
                model.addRow(row);
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Error loading class types: " + ex.getMessage());
        }
    }
     
